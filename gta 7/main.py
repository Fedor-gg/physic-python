V0 = 0  # Начальная скорость (м/с)
a = 2  # Ускорение (м/с²)
t_max = 10  # Максимальное время наблюдения (с)

# Заголовок таблицы
print(f'{"Время (с)":<10} {"Скорость (м/с)":<15} {"Пройденный путь (м)":<15}')

# Цикл по каждому моменту времени от 0 до t_max включительно
for t in range(t_max + 1):  # диапазон включает конечное значение
    # Вычисления скорости и пути
    V_t = V0 + a * t  # формула для скорости
    S_t = V0 * t + 0.5 * a * t ** 2  # формула для пути

    # Вывод строки с результатами
    print(f'{t:<10} {V_t:<15} {S_t:<15}')